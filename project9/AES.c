void Get_T() {
	int i;
	// 高位是行 
	for(i = 0; i < 256; i ++) {
		uint x_ = i % 16;
		uint y_ = i >> 4;
		uint x = s_box[y_][x_];
		uint num = (x << 16) + (x << 8);
		uint dx = ((x << 1) % 256) ^ (((x & 128) >> 7) * 27);
		uint sx = (dx ^ x);
		num += (dx << 24) + sx;
		T[y_][x_] = num;
	}
}

void InitInput() {
	int len = strlen(input);
	int i, j, js = 0;
	for(i = 0; i < 4; i ++) {
		for(j = 0; j < 4; j ++) {
			if(js == len) output[i][j] = 0;
			else output[i][j] = input[js ++];
		}
	}
}

void AddRoundKey() {
	int i, j, js = 0;
	for(i = 0; i < 4; i ++) {
		for(j = 0; j < 4; j ++) {
			output[i][j] ^= key[i][j];
		}
	}
}

uint Shift(uint a, int b) {
	// a 循环移位向右 b 位
	uint c = 1, i;
	for(i = 0; i < b; i ++) c = c * 2;
	c = a % c;
	a >>= b;
	a += (c << (32 - b));
	return a;
} 

void Get_key(int round) {
	int c = imp[round - 1];
	int i, j;
	int ls = key[0][3]; keynxt[0][3] = key[1][3]; keynxt[1][3] = key[2][3]; keynxt[2][3] = key[3][3]; keynxt[3][3] = ls;
	for(i = 0; i < 4; i ++) {
		int num = keynxt[i][3];
		keynxt[i][0] = s_box[num >> 4][num % 16];
		keynxt[i][0] ^= key[i][0];
		if(i == 0) keynxt[i][0] ^= c;
	}
	for(i = 1; i < 4; i ++) {
		for(j = 0; j < 4; j ++) {
			keynxt[j][i] = (keynxt[j][i - 1] ^ key[j][i]);
		}
	}
	for(i = 0; i < 4; i ++) {
		for(j = 0; j < 4; j ++) {
			key[i][j] = keynxt[i][j] % 256;
		}
	}
}

void Shiftoutput() {
	int i, j;
	/* 循环移位 */	
	i = output[1][0]; output[1][0] = output[1][1]; output[1][1] = output[1][2]; output[1][2] = output[1][3]; output[1][3] = i;
	i = output[2][0]; j = output[2][1]; output[2][0] = output[2][2]; output[2][1] = output[2][3]; output[2][2] = i; output[2][3] = j;
	i = output[3][3]; output[3][3] = output[3][2]; output[3][2] = output[3][1]; output[3][1] = output[3][0]; output[3][0] = i;
}

